package stringAndStringBuilder;

public class DiscussStringIntro {

	public static void main(String[] args) {
		
		String s = "Rockingamer";
		
		System.out.println("Length of the string = " + s.length());
		
		System.out.println("Character at index 2 = " + s.charAt(2));
		
		System.out.println("Substring from index 1 to index 5 = "+ s.substring(1,6));
		
		System.out.println("Substring from index 1 to end of the string = "+ s.substring(1));
	}

}
